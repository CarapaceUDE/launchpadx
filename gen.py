lines.append("fn content_type_for(path: &Path) -> &'static str {")
lines.append('    match path.extension().and_then(|e| e.to_str()) {')
lines.append('        Some("html") => "text/html; charset=utf-8",')
lines.append('        Some("js") | Some("mjs") => "application/javascript; charset=utf-8",')
lines.append('        Some("css") => "text/css; charset=utf-8",')
lines.append('        Some("json") => "application/json; charset=utf-8",')
lines.append('        Some("png") => "image/png",')
lines.append('        Some("jpg") | Some("jpeg") => "image/jpeg",')
lines.append('        Some("svg") => "image/svg+xml",')
lines.append('        Some("ico") => "image/x-icon",')
lines.append('        Some("woff2") => "font/woff2",')
lines.append('        Some("woff") => "font/woff",')
lines.append('        Some("ttf") => "font/ttf",')
lines.append('           _ => "application/octet-stream",')
lines.append("       }")
lines.append("}")
