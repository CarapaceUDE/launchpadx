lines.append("fn handle_request(dist_dir: &PathBuf, state: &Arc<Mutex<RpcState>>, mut request: tiny_http::Request) {")
lines.append("    let uri = request.url();")
lines.append("         // RPC endpoint")
lines.append('    if uri == "/rpc" && *request.method() == tiny_http::Method::Post {')
lines.append("        let body_str = std::io::read_to_string(request.as_reader()).unwrap_or_default();")
lines.append('        let request_data: serde_json::Value = match serde_json::from_str(&body_str) {')
lines.append("            Ok(d) => d,")
lines.append('            Err(e) => {')
lines.append('                let resp = make_json_response(400, None, Some(format!("Invalid JSON: {e}")));')
lines.append("                let _ = request.respond(resp);")
lines.append("                return;")
lines.append("               }")
lines.append("           };")
lines.append('        let method = request_data.get("method").and_then(|m| m.as_str()).unwrap_or("");')
lines.append('        let params = request_data.get("params").unwrap_or(&serde_json::Value::Null);')
lines.append('        let result = handle_rpc(&state.lock().unwrap(), method, params.clone());')
lines.append('        let resp = make_json_response(200, Some(result), None);')
lines.append("        let _ = request.respond(resp);")
lines.append("        return;")
lines.append("       }")
lines.append("         // Static file serving")
lines.append("    let clean_path = uri.trim_start_matches('/').trim_start_matches(\"index.html\");")
lines.append("    let file_path = dist_dir.join(clean_path);")
lines.append("         // Path traversal protection")
lines.append('    if !file_path.starts_with(dist_dir) {')
lines.append('        let _ = request.respond(make_json_response(403, None, Some("Forbidden".to_string())));')
lines.append("        return;")
lines.append("       }")
lines.append("         // SPA fallback: root maps to index.html")
lines.append('    let file_path = if clean_path.is_empty() || clean_path == "index.html" {')
lines.append('        dist_dir.join("index.html")')
lines.append("       } else {")
lines.append("        file_path")
lines.append("       };")
lines.append('    match std::fs::read(&file_path) {')
lines.append("        Ok(contents) => {")
lines.append('            let ct = content_type_for(&file_path);')
lines.append('            let resp = make_file_response(200, ct, contents);')
lines.append("            let _ = request.respond(resp);")
lines.append("           }")
lines.append("        Err(_) => {")
lines.append("               // SPA fallback")
lines.append('            if let Ok(index_html) = std::fs::read(dist_dir.join("index.html")) {')
lines.append('                let resp = make_file_response(200, "text/html", index_html);')
lines.append("                let _ = request.respond(resp);")
lines.append("               } else {")
lines.append('                let resp = make_json_response(404, None, Some("Not Found".to_string()));')
lines.append("                let _ = request.respond(resp);")
lines.append("               }")
lines.append("           }")
lines.append("       }")
lines.append("}")

